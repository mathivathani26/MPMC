        OUTPUT 2500AD
.ORG 2000H
DATA SEGMENT
C_VAL  DB 39H      ; C
S_VAL  DB 6DH      ; S
E_VAL  DB 79H      ; E
OFF    DB 00H
DATA ENDS
CODE SEGMENT
    ASSUME CS:CODE, DS:DATA
START:
    MOV AX,DATA
    MOV DS,AX
    MOV AL,80H
    MOV DX,FFE6H
    OUT DX,AL
MAIN:
    MOV DX,FFE0H      ; LED1
    MOV AL,C_VAL
    OUT DX,AL
    MOV DX,FFE2H      ; LED2
    MOV AL,S_VAL
    OUT DX,AL
    MOV DX,FFE4H      ; LED3
    MOV AL,E_VAL
    OUT DX,AL
    CALL DELAY
    MOV DX,FFE0H
    MOV AL,00H
    OUT DX,AL
    MOV DX,FFE2H
    OUT DX,AL
    MOV DX,FFE4H
    OUT DX,AL
    CALL DELAY
    JMP MAIN
DELAY:
    MOV AH,05H
L3:
    MOV BX,FFFFH
L2:
    DEC BX
    JNZ L2
    DEC AH
    JNZ L3
    RET
CODE ENDS
END 



